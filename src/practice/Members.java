package practice;

public class Members {
	
	int num;
	String id;
	String pass;
	String name;
	
	Members(int num, String id, String pass, String name){
		
		this.num = num;
		this.id = id;
		this.pass = pass;
		this.name = name;
		
	}
	Members(){
		this.num = 0;
		this.id = "";
		this.pass = "";
		this.name = "";
		
	}
}
